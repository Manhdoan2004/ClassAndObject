package advance.dev;

public class Student {
	String name;
	int age;
	String address;
	String phoneNumber;
	double averageGrade;

	public Student(String name, int age, String address, String phoneNumber, double averageGrade) {
		this.name = name;
		this.age = age;
		this.address = address;
		this.phoneNumber = phoneNumber;
		this.averageGrade = averageGrade;
	}
}
